class Pixel{          //This class represents a Pixel. A pixel is composed of 3 colours.
      int red;
      int green;
      int blue;
      public Pixel(){this(0,0,0);}
      public Pixel(int pRed,int pGreen, int pBlue){
        this.red=pRed;
        this.green=pGreen;
        this.blue=pBlue;

      }
}
